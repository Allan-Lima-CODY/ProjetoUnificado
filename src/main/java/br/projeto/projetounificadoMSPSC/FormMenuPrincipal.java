    public void atualizar(String user, String pass) {
        String nomeComp = txtNomeCompleto.getText();
        String email = txtEmail.getText();
        String fone = txtTelefone.getText();
        int id = Integer.parseInt(txtIDUsuario.getText());
                
        try {
            FormLogin.userLogin.alterarDadosCadastrais(id, nomeComp, user, email, pass, fone);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Por algúm motivo, não foi possível gravar os dados alterados!");
        }
                
        JOptionPane.showMessageDialog(null, "Dados alterados com sucesso!");
                
        habilitarBotoes();
        desabilitarCampos();
        btnSalvar.setVisible(false);
    }
    
