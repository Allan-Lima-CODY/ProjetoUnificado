    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String user = txtUsuario.getText();
        boolean validado = FormLogin.userLogin.validarUsuario(user);
        
        String pass = String.valueOf(txtSenha.getPassword());
        
        if (txtNomeCompleto.getText().isEmpty() || txtUsuario.getText().isEmpty() || txtEmail.getText().isEmpty() || 
            pass.isEmpty() || txtTelefone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para que o sistemas possa alterar seus dados!");
        }
        else {
            boolean mesmoNome = (FormLogin.userLogin.getUsuario().equals(user)) ? true : false;
            
            if (mesmoNome) {
                atualizar(user, pass);
            }
            else if (validado) {
                atualizar(user, pass);
            }
            else {
                JOptionPane.showMessageDialog(null, "Esse nome de usuário já existe no sistema. Use um diferente!");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

