    private void btnExcluirContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirContaActionPerformed
        // TODO add your handling code here:
        String alerta = "Tem certeza que deseja excluir sua conta?\nTodos os projetos e requisitos"
                + " criados por você serão excluídos juntamente com a sua conta!";
        
        int confirm = JOptionPane.showConfirmDialog(null, alerta);
        
        if (confirm == JOptionPane.YES_OPTION) {
            FormLogin.userLogin.excluirConta();
            
            FormLogin login = new FormLogin();
            login.setVisible(true);
            this.setVisible(false);
        }
        else if (confirm == JOptionPane.NO_OPTION) {}
    }//GEN-LAST:event_btnExcluirContaActionPerformed

