    private void btnConsultarProjetoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarProjetoActionPerformed
        // TODO add your handling code here:
        if(tblProjetos.getSelectedRow() != -1) {
            getSelectedProject();
        
            txtNomeProjeto.setText(projetoForm.getNome());
            txtDescricaoProjeto.setText(projetoForm.getDescricao());
            txtUsuarioProprietario.setText(projetoForm.getUsuarioProprietario());
        
            boolean verificado = projetoForm.verificarUsuarioProprietario();
        
            if (verificado) {
                btnAlterarProjeto.setEnabled(true);
                btnRemoverProjeto.setEnabled(true);
            }
            else {
                btnAlterarProjeto.setEnabled(false);
                btnRemoverProjeto.setEnabled(false);
            }
        
            btnConsultarRequisitos.setEnabled(true);
        }
    }//GEN-LAST:event_btnConsultarProjetoActionPerformed

