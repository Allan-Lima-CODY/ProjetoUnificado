    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String nome = txtNomeProjeto.getText();
        String desc = txtDescricaoProjeto.getText();
        
        boolean validado = projetoForm.autenticarProjeto(nome);
        
        if (txtNomeProjeto.getText().isEmpty() || txtDescricaoProjeto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para que o sistemas possa alterar seus dados!");
        }
        else {
            boolean mesmoNome = (projetoForm.getNome() == nome) ? true : false;
            
            if (mesmoNome) {
                atualizar();
                projetoForm.setNome(nome);
                projetoForm.setDescricao(desc);
                btnConsultarRequisitos.setEnabled(true);
                btnRelatorio.setEnabled(true);
            }
            else if (validado) {
                atualizar();
                projetoForm.setNome(nome);
                projetoForm.setDescricao(desc);
                btnConsultarRequisitos.setEnabled(true);
                btnRelatorio.setEnabled(true);
            }
            else {
                JOptionPane.showMessageDialog(null, "Já existe um projeto com esse nome cadastrado no sistema. Use um diferente!");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnRemoverProjetoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverProjetoActionPerformed
        // TODO add your handling code here:
        String alerta = "Tem certeza que deseja excluir esse projeto?\nTodos os requisitos contidos nele serão deletados também!";
        
        int confirm = JOptionPane.showConfirmDialog(null, alerta);
        
        if (confirm == JOptionPane.YES_OPTION) {
            btnAlterarProjeto.setEnabled(false);
            btnRemoverProjeto.setEnabled(false);
            
            txtDescricaoProjeto.setText("");
            txtNomeProjeto.setText("");
            txtUsuarioProprietario.setText("");
            
            projetoForm.setNome("");
            projetoForm.setDescricao("");
            projetoForm.setUsuarioProprietario("");
            
            projetoForm.deletarProjeto();
            
            projetoForm.setId(0);
            
            recuperarListaProjeto();
            
            btnConsultarRequisitos.setEnabled(false);
            btnRelatorio.setEnabled(false);
        }
        else if (confirm == JOptionPane.NO_OPTION) {}
    }//GEN-LAST:event_btnRemoverProjetoActionPerformed

