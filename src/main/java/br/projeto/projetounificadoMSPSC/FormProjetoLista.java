    public void getSelectedProject() {
        try {
            if (tblProjetos.getSelectedRow() != -1) {
                int idProjeto = tblProjetos.getValueAt(tblProjetos.getSelectedRow(), 0).hashCode();

                projetoForm.getDataProjects(idProjeto);
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Não foi possível resgatar o valor do ID!");
        }
    }
    
    public void atualizar() {
        String nomeProjeto = txtNomeProjeto.getText();
        String desc = txtDescricaoProjeto.getText();
        
        projetoForm.alterarDadosDoProjeto(nomeProjeto, desc);
        
        recuperarListaProjeto();
        
        JOptionPane.showMessageDialog(null, "Projeto alterado com sucesso!");
        
        tblProjetos.setEnabled(true);
        txtNomeProjeto.setEnabled(false);
        txtDescricaoProjeto.setEnabled(false);
        btnRemoverProjeto.setEnabled(true);
        btnVoltar.setEnabled(true);
        btnConsultarProjeto.setEnabled(true);
        btnAlterarProjeto.setEnabled(true);
        btnCancel.setVisible(false);
        btnSalvar.setVisible(false);
    }

