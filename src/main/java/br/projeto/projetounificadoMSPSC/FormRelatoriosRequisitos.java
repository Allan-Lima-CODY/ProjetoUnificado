    public ResultSet carregaTabela(String tipo, String arg) throws SQLException {
        String argumento = tipo + " " + "LIKE \"%" + arg + "%\"";
        
        String sql = "SELECT R.ID, R.Identificador, R.Nome, R.Modulo, R.Funcionalidades, R.Prioridade, R.Descricao, P.Nome AS Projeto, "
                   + "U.NomeUsuario AS Autor "
                   + "FROM Requisitos R "
                   + "LEFT JOIN Usuarios U ON (R.Autor = U.ID) "
                   + "LEFT JOIN Projetos P ON (R.Projeto = P.ID)"
                   + "WHERE " + argumento + " AND P.ID = " + FormProjetoLista.projetoForm.getId();
        
            ConnectionFactory conexaoBD = new ConnectionFactory();
            Connection conn = conexaoBD.getConnection();
        
            Statement stm = conn.createStatement();
            
            ResultSet resultado; 
            resultado = stm.executeQuery(sql);
            
            return resultado;
    }

