    private void txtFiltroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroKeyReleased
        // TODO add your handling code here:
        String tipo = "";
        
        String escolha = cbFiltro.getSelectedItem().toString().trim();
        
        if (escolha.equals("Identificador")) {
            tipo = " " + "R.Identificador";
        }
        if (escolha.equals("Nome")) {
            tipo = " " + "R.Nome";
        }
        if (escolha.equals("Módulo")) {
            tipo = " " + "R.Modulo";
        }
        if (escolha.equals("Funcionalidades")) {
            tipo = " " + "R.Funcionalidades";
        }
        if (escolha.equals("Prioridade")) {
            tipo = " " + "R.Prioridade";
        }
        if (escolha.equals("Descrição")) {
            tipo = " " + "R.Descricao";
        }
        if (escolha.equals("Autor")) {
            tipo = " " + "Autor";
        }
        
        String arg = txtFiltro.getText();
        
        DefaultTableModel mp1 = (DefaultTableModel) tblRequisitos.getModel();
        
        int l = mp1.getRowCount();
        
        if(l>0) {
            while(l>0) {
                ((DefaultTableModel) tblRequisitos.getModel()).removeRow(l-1);
                l--;
            }
        }
        
        try {
            ResultSet rs = carregaTabela(tipo, arg);
            
            DefaultTableModel mp = (DefaultTableModel) tblRequisitos.getModel();
            
            while(rs.next()){
                String Coluna0 = rs.getString("R.ID").toString().trim();
                String Coluna1 = rs.getString("R.Identificador").toString().trim();
                String Coluna2 = rs.getString("R.Nome").toString().trim();
                String Coluna3 = rs.getString("R.Modulo").toString().trim();
                String Coluna4 = rs.getString("R.Funcionalidades").toString().trim();
                String Coluna5 = rs.getString("R.Prioridade").toString().trim();
                String Coluna6 = rs.getString("R.Descricao").toString().trim();
                String Coluna7 = rs.getString("Autor").toString().trim();
                
                mp.addRow(new String[]{Coluna0, Coluna1, Coluna2, Coluna3, Coluna4, Coluna5, Coluna6, Coluna7});
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher a coluna com os dados solicitados!");
        }
    }//GEN-LAST:event_txtFiltroKeyReleased

